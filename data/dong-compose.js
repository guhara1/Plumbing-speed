// 문단 통째로 고르는 방식이 아니라, 각 문단을 여러 문장 슬롯의 조합으로 생성하고,
// 9개 테마 중 5개를 시드로 선택·순서변경하여 페이지마다 서로 다른 본문을 만듭니다.
// 이렇게 하면 문장 조합 경우의 수가 매우 커져(테마당 수백 가지 × 선택/순서),
// 페이지 간 중복·복사·도어웨이 위험을 크게 낮춥니다. (빌드 시 중복 검사기로 실제 검증)
// {D}=동네명, {G}=직속 상위(구 또는 시)명.

function mulberry32(a) {
  return function () {
    a |= 0;
    a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}
function hashStr(s) {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = (h * 16777619) >>> 0;
  }
  return h >>> 0;
}
