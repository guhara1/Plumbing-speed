// seedStr 로부터 페이지 고유 본문 생성
function composeDong(seedStr, D, G) {
  const rng = mulberry32(hashStr(seedStr));
  const pick = (pool) => pool[Math.floor(rng() * pool.length)];
  const F = (s) => fill(s, D, G);

  const intro = [F(pick(introOpen)), F(pick(introMid)), F(pick(introClose))].join(" ");

  // 9개 테마를 셔플해 5개 선택
  const order = themes.map((t, i) => [rng(), i]).sort((a, b) => a[0] - b[0]).map((x) => x[1]);
  const chosen = order.slice(0, 5);
  const sections = chosen.map((ti) => {
    const t = themes[ti];
    const body = [F(pick(t.a)), F(pick(t.b)), F(pick(t.c))].join(" ");
    return { h2: F(pick(t.h2)), body };
  });

  const fx = pick(faqExtraPool);
  const faqExtra = { q: F(fx.q), a: F(fx.a) };

  return { intro, sections, faqExtra };
}

module.exports = { composeDong };
